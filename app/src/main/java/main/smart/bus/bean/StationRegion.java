package main.smart.bus.bean;

public class StationRegion {

	private float x;
	private float y;
	private float maxX;
	private float maxY;
	private float minX;
	private float minY;
	private int sxx;
	private int station;
	
	
	public int getSxx() {
		return sxx;
	}
	public void setSxx(int sxx) {
		this.sxx = sxx;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public float getMaxX() {
		return maxX;
	}
	public void setMaxX(float maxX) {
		this.maxX = maxX;
	}
	public float getMaxY() {
		return maxY;
	}
	public void setMaxY(float maxY) {
		this.maxY = maxY;
	}
	public float getMinX() {
		return minX;
	}
	public void setMinX(float minX) {
		this.minX = minX;
	}
	public float getMinY() {
		return minY;
	}
	public void setMinY(float minY) {
		this.minY = minY;
	}
	public int getStation() {
		return station;
	}
	public void setStation(int station) {
		this.station = station;
	}
}
